# Improved function to handle and clean Unicode characters
def clean_category_text(text):
    if isinstance(text, str):
        return codecs.decode(text, 'unicode_escape')
    return text

# Helper function to fetch category from PokéAPI
def fetch_pokemon_category(name):
    name_formatted = name.lower().replace(" ", "-").replace("'", "").replace(":", "")
    response = requests.get(f"https://pokeapi.co/api/v2/pokemon-species/{name_formatted}")
    
    if response.status_code == 200:
        species_data = response.json()
        category = next((genus["genus"] for genus in species_data["genera"] if genus["language"]["name"] == "en"), "Unknown")
        return category
    else:
        print(f"Failed to fetch category for {name}")
        return "Unknown"

# Enhanced fetch function to handle variations in naming conventions
def fetch_pokemon_data(name):
    name_formatted = name.lower().replace(" ", "-").replace("'", "").replace(":", "")
    response = requests.get(f"https://pokeapi.co/api/v2/pokemon/{name_formatted}")
    
    if response.status_code == 200:
        pokemon_data = response.json()
        
        # Extract and return data structure
        return {
            "name": pokemon_data["name"],
            "pokedex_number": pokemon_data["id"],
            "types": [type_info["type"]["name"] for type_info in pokemon_data["types"]],
            "abilities": [ability["ability"]["name"] for ability in pokemon_data["abilities"]],
            "stats": {stat["stat"]["name"]: stat["base_stat"] for stat in pokemon_data["stats"]},
            "height": pokemon_data["height"] / 10,
            "weight": pokemon_data["weight"] / 10,
            "image": pokemon_data["sprites"]["front_default"],
            "shiny_image": pokemon_data["sprites"].get("front_shiny") if name_formatted in shiny_pokemon_names else None,
            "category": fetch_pokemon_category(name)
        }
    else:
        print(f"Failed to fetch data for {name}")
        return None

# Backup function
def backup_pokemon_data(filename):
    backup_filename = f"{filename}.backup"
    if os.path.exists(filename):
        with open(filename, "r", encoding="utf-8") as file:
            data = file.read()
        with open(backup_filename, "w", encoding="utf-8") as backup_file:
            backup_file.write(data)
        print(f"Backup created: {backup_filename}")
    else:
        print("Original file does not exist; no backup created.")

# Save updated Pokémon data to JSON file with UTF-8 encoding
def save_pokemon_data(pokemon_data, filename):
    try:
        with open(filename, "w", encoding="utf-8") as file:
            json.dump(pokemon_data, file, indent=4, ensure_ascii=False)
        print(f"Updated data saved to {filename}")
    except UnicodeEncodeError as e:
        print(f"Error saving data: {e}")

# Load existing Pokémon data from JSON file
def load_pokemon_data(filename):
    try:
        with open(filename, "r", encoding="utf-8") as file:
            return json.load(file)
    except json.JSONDecodeError as e:
        print(f"Error loading JSON data: {e}")
        return []

# Update existing Pokémon data with cleaned categories
def update_pokemon_categories(filename):
    pokemon_data = load_pokemon_data(filename)
    
    if not pokemon_data:
        print("Failed to load Pokémon data. Exiting.")
        return
    
    for pokemon in pokemon_data:
        if "category" in pokemon:
            # Clean up any category text to fix Unicode issues
            pokemon["category"] = clean_category_text(pokemon["category"])
    
    save_pokemon_data(pokemon_data, filename)

